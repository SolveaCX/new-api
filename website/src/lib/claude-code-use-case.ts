export const CLAUDE_CODE_INSTALL_COMMANDS = {
  macos: `curl -fsSL https://flatkey.ai/install.sh | bash`,
  linux: `curl -fsSL https://flatkey.ai/install.sh | bash`,
  windows: `iwr https://flatkey.ai/install.ps1 -UseBasicParsing | iex`,
} as const;

export type ClaudeCodeInstallTab = keyof typeof CLAUDE_CODE_INSTALL_COMMANDS;

export function detectClaudeCodeInstallTab(platform: string): ClaudeCodeInstallTab {
  const normalized = platform.toLowerCase();
  if (normalized.includes("win")) return "windows";
  if (normalized.includes("linux")) return "linux";
  return "macos";
}
